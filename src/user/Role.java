package user;
/**
 * Enumeration File representing the various roles of users in the system
 * This defines the role types that users can have within the system
 */

public enum Role {
    /** represents a Patient role in HMS */
    Patient,
    /** represents a Doctor role in HMS */
    Doctor,
    /** represents a Pharmacist role in HMS */
    Pharmacist,
    /** represents a Administrator role in HMS */
    Administrator
}
